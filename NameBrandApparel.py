def add_to_cart(inventory, shopping_cart, article_choice, brand_choice, color_choice, size_choice, price):
    cart_choice = input('Would you like to add this item to your cart? Yes/No ').lower()
    if cart_choice == 'yes':
        shopping_cart[article_choice] = {
            brand_choice: {
                color_choice: {
                    size_choice: price
                }
            }
        }
        print(f'You have added {size_choice.title()} {color_choice.title()} {brand_choice.title()} {article_choice} to your cart')
        
    elif cart_choice == 'no':
        main(inventory, shopping_cart)
    else:
        print('Please enter a valid input')
        add_to_cart(inventory, shopping_cart, article_choice, brand_choice, color_choice, size_choice, price)
    



def show_cart(inventory, shopping_cart):
    if shopping_cart:
        for article in shopping_cart:
            for brand in shopping_cart[article]:
                for color in shopping_cart[article][brand]:
                    for size in shopping_cart[article][brand][color]:
                        print(f'''
=======================================================
{color} {brand} {article} - {size} - ${shopping_cart[article][brand][color][size]}''')

        choice = input("""
            1. Remove Item From Cart
            2. Checkout
            3. Return
                    """)
        if choice == '1':
            remove_from_cart(inventory, shopping_cart)
        elif choice == '2':
            checkout(inventory, shopping_cart)
        elif choice == '3':
            main(inventory, shopping_cart)
        else:
            print('Please enter a valid input')
            show_cart(inventory, shopping_cart)
    else:
        print('Your cart is empty')
        main(inventory, shopping_cart)
            


def remove_from_cart(inventory, shopping_cart):
    if shopping_cart:
        article_choice = input('What type of clothing would you like to remove from your cart? ').lower()
        brand_choice = input('What brand is the article of clothing? ').lower()
        color_choice = input('What color is the article? ').lower()
        size_choice = input('What size is it? ').lower()
        if article_choice in shopping_cart:
            if brand_choice in shopping_cart[article_choice]:
                if color_choice in shopping_cart[article_choice][brand_choice]:
                    if size_choice in shopping_cart[article_choice][brand_choice][color_choice]:
                        del shopping_cart[article_choice][brand_choice][color_choice][size_choice]
                        print(f'{size_choice.title()} {color_choice.title()} {brand_choice.title()} {article_choice} has been removed from your cart')
                        show_cart(inventory, shopping_cart)
                    else:
                        print('Size not found. Please try again.')
                        remove_from_cart(inventory, shopping_cart)
                else:
                    print('Color not found. Please try again.')
                    remove_from_cart(inventory, shopping_cart)
            else:
                print('Brand not found. Please try again.')
                remove_from_cart(inventory, shopping_cart)
        else:
            print('Article not found. Please try again.')
            remove_from_cart(inventory, shopping_cart)
    else:
        print('Your cart is empty')
        main(inventory, shopping_cart)



def checkout(inventory, shopping_cart):
    total = 0
    for article in shopping_cart:
        for brand in shopping_cart[article]:
            for color in shopping_cart[article][brand]:
                for size in shopping_cart[article][brand][color]:
                    total += shopping_cart[article][brand][color][size]
    print(f'Your total is: ${total}')
    print('Thank you for shopping with us!')
    shopping_cart.clear()
    main(inventory, shopping_cart)



def main(inventory, shopping_cart):
    while True:
        response = input("""
          
     NAME BRAND APPAREL
  ///////////////////////    
    1. View Collection
    2. Show Cart
    3. Close Tab
    
    
    """)
        if response == '1' or response.lower() == 'view collection':
            show_clothing(inventory, shopping_cart)
        elif response == '2' or response.lower() == 'show cart':
            show_cart(inventory, shopping_cart)
        elif response == '3' or response.lower() == 'close tab':
            break
        else:
            print('Please enter a valid input')
        
main(inventory, shopping_cart)